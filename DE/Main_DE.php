<h2>Warum Musik draußen?</h2>
<p>
Natur ist an sich Musik, Naturerlebnis ist an sich Therapie.</br> Was ist denn Musiktherapie in/mit der Natur? </br>
Natur als Spielraum, da dort der Raum offen für dich aber auch beschützend sein kann.
</p>

<h2>Was ist Improvisation?</h2>
<p>
Musik ohne Musiknoten - Reise ohne Landkarte - warum nicht, wenn es uns einfach Spaß macht? </br>
Improvisation klingt erstmal abschreckend für Nicht-Musiker, aber genau für sie ist die freie Form des Musizierens richtig und wichtig.</br>
Keine Angst, man kann da nichts falsch machen.</br>
Aus deinen Fingern kommt deine eigene Musik, die sich von keinen anderen Menschen nachahmen lässt.
