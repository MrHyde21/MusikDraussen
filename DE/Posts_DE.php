		<h3>Wenn G-Dur für mich gelb wie Morgenlicht ist</h3>
		
		<span class="date">13.12.2016 </span><p>
		<p>
		In meiner Unizeit in Japan haben meine Kommilitonen und ich oft diskutiert, welche Farbe das Kleid für das anstehende Konzert haben sollte. „Das Stück klingt so wie blau, also nehme ich ein blaues Kleid.“ oder „Zu deinem Stück passt rot.“</br>
		Außerdem hat eine mir erzählt, dass sie die Zahlen als Person wahrnimmt. „1 ist ein kleiner Junge, 4 ist eine junge stolze Frau..“ 
		</p>
		<p>
		Deswegen hatte ich meine Empfindung gar nicht als sonderliches Phänomen registriert. Dass ich bestimmte Farbe an Klaviertasten, Buchstabe und Wochentag sehe, hat mir auch beim Erinnern geholfen. Nur wusste ich nicht, dass meine Empfindung einen Namen hat. Das bezeichnet man als „Synästhesie“. 
		</p>
		<p>
		Unter Synästhesie versteht man eine Querverbindung im Gehirn, die verschiedene Sinnesempfindungen aneinander verknüpft. Diese Verbindung besteht zuerst bei allen Neugeborenen, die meist im Laufe der Entwicklung verloren geht. Das ist ein neurologisches Phänomen, was nicht zwingend eine Störung darstellt. Infolge davon nimmt man die Sachen auch mit anderen Sinnesorgan wahr. Wie etwa Musik riechen oder sehen, schwarze Buchstabe als farbig wahrnehmen. 
		</p>
