<h2>Why music outside?</h2>
<p>
Nature is in itself music, the experience of nature is therapy in itself.</br> What is then music therapy in/with nature? </br>
Nature as a playroom, since the space can be open for you but also protective.
</p>

<h2>What is improvisation?</h2>
<p>
Music without music notes - travel without a map - why not, if it is fun? </br>
Improvisation sounds first dissuasive for non-musician, but for them is the free form of the music making right and important.</br>
Don't worry, you can't go wrong there.</br>
From your fingers comes your own music that can not be imitated by any other human being.
